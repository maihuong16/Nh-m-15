
CREATE DATABASE QL_Lichday_GV
USE QL_Lichday_GV

CREATE TABLE KHOA ( 
MAKHOA VARCHAR(10)  PRIMARY KEY, 
TENKHOA NVARCHAR (40) , 
DTKHOA NVARCHAR (20)     
) 

CREATE TABLE GIAOVIEN ( 
MAGV VARCHAR (5) PRIMARY KEY , 
HOTEN NVARCHAR (40) , 
DTGV NVARCHAR (20),
MAKHOA VARCHAR(10) FOREIGN KEY REFERENCES KHOA (MAKHOA)     
) 

CREATE TABLE  MONHOC (
MAMH VARCHAR  (10) PRIMARY KEY , 
TENMH NVARCHAR (40)  
) 

CREATE TABLE  PHONGHOC (
PHONG VARCHAR (10) PRIMARY KEY, 
CHUCNANG NVARCHAR (40)   
)

CREATE TABLE LOP ( 
MALOP VARCHAR (10) PRIMARY KEY, 
TENLOP NVARCHAR (40) , 
SISO INT, 
MAKHOA VARCHAR (10)  FOREIGN KEY REFERENCES KHOA (MAKHOA)    
) 

CREATE TABLE  LICHDAY ( 
MAGV VARCHAR (5) FOREIGN KEY REFERENCES GIAOVIEN (MAGV) , 
MALOP VARCHAR (10) FOREIGN KEY REFERENCES LOP (MALOP),  
MAMH VARCHAR (10) FOREIGN KEY REFERENCES MONHOC (MAMH) , 
PHONG VARCHAR (10) FOREIGN KEY REFERENCES PHONGHOC (PHONG), 
NGAYDAY DATETIME, 
TUTIET INT, 
DENTIET INT, 
BAIDAY NVARCHAR (200), 
GHICHU NVARCHAR (200), 
LYTHUYET INT, 
CONSTRAINT PK_LICHDAY PRIMARY KEY ( MAGV, MALOP, MAMH, PHONG) 
) 


